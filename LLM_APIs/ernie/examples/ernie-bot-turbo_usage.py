import requests
import json

def get_access_token():
    """
    Use the API Key and Secret Key to obtain an access_token, replacing the example's application API Key and Secret Key below.
    """
    
    url = "https://aip.baidubce.com/oauth/2.0/token?grant_type=client_credentials&client_id=[Application API Key]&client_secret=[Application Secret Key]"
    
    payload = json.dumps("")
    headers = {
        'Content-Type': 'application/json',
        'Accept': 'application/json'
    }
    
    response = requests.request("POST", url, headers=headers, data=payload)
    return response.json().get("access_token")

def main():
    url = "https://aip.baidubce.com/rpc/2.0/ai_custom/v1/wenxinworkshop/chat/completions_pro?access_token=" + get_access_token()
    
    payload = json.dumps({
        "messages": [
            {
                "role": "user",
                "content": "Tell me about Beijing"
            }
        ]
    })
    headers = {
        'Content-Type': 'application/json'
    }
    
    response = requests.request("POST", url, headers=headers, data=payload)
    
    print(response.text)
    

if __name__ == '__main__':
    main()
